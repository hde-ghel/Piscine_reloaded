int	ft_iterative_factorial(int nb)
{
	int res;
	int i;

	res = 1;
	i = nb;
	if (nb >= 0)
	{
		while (i >= 2)
		{
			res = res * i;
			i--;
		}
		return (res);
	}
	return (0);
}
