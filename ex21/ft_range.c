#include <stdlib.h>

int	*ft_range(int min, int max)
{
	int i;
	int *range;

	if (min >= max)
		return (NULL);
	if (!(range = (int *)malloc(sizeof(int) * (max - min))))
		return (NULL);
	i = 0;
	while (min < max)
	{
		range[i] = min;
		i++;
		min++;
	}
	return (range);
}
